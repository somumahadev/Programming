#include <stdio.h>

int main() {
    int integerVariable = 10;
    float floatVariable = 3.14;
    char charVariable = 'A';
    
    printf("Integer Variable: %d\n", integerVariable);
    printf("Float Variable: %f\n", floatVariable);
    printf("Char Variable: %c\n", charVariable);
    
    return 0;
}

